# Since swirl is an R package, you can easily install it by 
# entering a single command from the R console
install.packages("swirl")

# Load swirl
library(swirl)

# Install the R Progroamming course
install_from_swirl("R Programming")

# Start swirl and complete the lessons
swirl()

